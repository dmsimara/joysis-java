
package com.stage1;


public class RelationalOperator {
    
    public static void main(String[] args) {
        
        // comparison - <, >, >=, <=
        int num1 = 10;
        int num2 = 20;
        
        System.out.println(num1 < num2);
        System.out.println(num1 > num2);
        System.out.println(num1 <= num2);
        System.out.println(num1 >= num2);
        
        System.out.println(" ");
                
        // Equality - == and !=
        System.out.println(num1 == num2);
        System.out.println(num1 != num2);
    }
}
